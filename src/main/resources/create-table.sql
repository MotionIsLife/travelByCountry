CREATE TABLE COUNTRY (
  ID INT NOT NULL AUTO_INCREMENT,
  COUNTRY_NAME VARCHAR(100) NOT NULL,
  UNIQUE UQ_COUNTRY_1 (COUNTRY_NAME),
  PRIMARY KEY (ID)
);
CREATE TABLE LANGUAGE (
  ID INT NOT NULL AUTO_INCREMENT,
  LANGUAGE_NAME VARCHAR(100) NOT NULL,
  COUNTRY_ID INT,
  UNIQUE UQ_LANGUAGE_1 (LANGUAGE_NAME),
  PRIMARY KEY (ID),
  CONSTRAINT FK_LANGUAGE_1 FOREIGN KEY (COUNTRY_ID) REFERENCES COUNTRY (ID)
);

CREATE TABLE CITY (
  ID INT NOT NULL AUTO_INCREMENT,
  CITY_NAME VARCHAR(100) NOT NULL,
  COUNTRY_ID INT,
  UNIQUE UQ_CITY_1 (CITY_NAME),
  PRIMARY KEY (ID),
  CONSTRAINT FK_CITY_2 FOREIGN KEY (COUNTRY_ID) REFERENCES COUNTRY (ID)
);

